"""Tier-1 metric tables per task, with per-language breakdown."""
from __future__ import annotations

import sys
from pathlib import Path

import streamlit as st

# Make `utils/` importable when Streamlit loads this page directly.
sys.path.insert(0, str(Path(__file__).resolve().parent.parent))

from utils import data as data_utils
from utils import render

st.set_page_config(page_title="Results — UPSC SLM v1", page_icon="📊", layout="wide")

st.title("📊 Tier-1 Results")
st.caption(
    "Per-task metric tables across the four conditions. All values are "
    "deterministic Tier-1 metrics computed by `scripts/score_tier1.py`. "
    "Means + 95 % bootstrap CI (N=1000, seed 20260514)."
)

# ---------- Sidebar: filters ----------
task = st.sidebar.selectbox(
    "Task",
    options=list(data_utils.TASK_LABELS.keys()),
    format_func=lambda t: f"{t} — {data_utils.TASK_LABELS[t]}",
    key="results_task",
)
language = render.language_picker(key="results_lang", default="all")

agg = data_utils.load_aggregate()

# Available metrics for the chosen task × language pair
metrics_available = sorted(
    agg[(agg["task"] == task) & (agg["language"] == language)]["metric"].unique().tolist()
)

if not metrics_available:
    st.warning(
        f"No aggregate rows for task={task}, language={language}. "
        f"This happens when a metric only applies to one language (e.g. `hindi_code_mixing_rate` is HI-only)."
    )
    st.stop()

# ---------- Headline metric front-and-center ----------
headline_metric = data_utils.HEADLINE_METRIC[task]
if headline_metric in metrics_available:
    st.header(f"Headline metric — {headline_metric}")
    render.render_metric_table(agg, task, headline_metric, language)
    st.divider()

# ---------- All other metrics in expanders ----------
st.header("All metrics for this task × language")
other_metrics = [m for m in metrics_available if m != headline_metric]

# Group metrics into thematic clusters for navigation
def _group_for(metric: str) -> str:
    if metric in {"is_correct", "format_fail", "refusal", "upsc_neg_marking_score"}:
        return "Correctness"
    if metric in {"brier_loss", "confidence_prob"} or "ece" in metric.lower():
        return "Calibration"
    if "bertscore" in metric or "rouge" in metric or "chrf" in metric or "bleurt" in metric:
        return "Similarity to gold"
    if (
        "entity" in metric
        or "citation" in metric
        or "hallucination" in metric
        or "fact" in metric
    ):
        return "Factuality"
    if "word_count" in metric or "sentence_count" in metric or "paragraph_count" in metric:
        return "Length / structure"
    if "hindi" in metric or "devanagari" in metric or "code_mixing" in metric:
        return "Hindi-specific"
    if "score" in metric or "qwk" in metric or "improvements" in metric or "strengths" in metric:
        return "Rubric grading"
    if metric in {"latency_ms", "ttft_ms", "tokens_per_sec", "cost_usd"}:
        return "Universal"
    return "Other"

grouped: dict[str, list[str]] = {}
for m in other_metrics:
    grouped.setdefault(_group_for(m), []).append(m)

order = [
    "Correctness", "Similarity to gold", "Length / structure",
    "Factuality", "Calibration", "Rubric grading",
    "Hindi-specific", "Universal", "Other",
]
for group in order:
    if group not in grouped:
        continue
    with st.expander(f"{group} ({len(grouped[group])})", expanded=(group == "Correctness")):
        for m in sorted(grouped[group]):
            render.render_metric_table(agg, task, m, language, caption=f"`{m}`")

# ---------- Universal metrics (latency / cost) — always shown ----------
if task == list(data_utils.TASK_LABELS.keys())[0]:
    # Show universal metrics on Task A page (they don't vary much by task).
    pass

st.sidebar.divider()
st.sidebar.caption(
    "Source: `results/aggregate.parquet` (auto-generated by "
    "`scripts/aggregate.py`). N is per-condition row count after filtering."
)
